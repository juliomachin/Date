package es.unileon.prg.tema7;


public class MainDate
{
	public static void main(String[] args)
	{
		Date date1, date2;
		date1 = new Date(1,5,2015);
		date2 = new Date (24,6,2011);
		System.out.println(date1.isSameDay(date2));
		System.out.println(date1.isSameMonth(date2));
		System.out.println(date1.isSameYear(date2));
		System.out.println(date1.isSame(date2));
		System.out.println(date1.getMonthName());
		System.out.println(date1.daysOfMonth());
		System.out.println(date1.isDayRigth());
		System.out.println(date1.getSeason());
		System.out.println(date1.getMonthsLeft());
		System.out.println(date1.printDate());
		System.out.println(date1.sameDays());
		System.out.println(date1.getDaysLeft());
	}
}
